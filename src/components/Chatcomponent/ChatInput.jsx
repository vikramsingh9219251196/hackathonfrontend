import React, { useState, useEffect } from "react";
import { BsEmojiSmileFill } from "react-icons/bs";
import { IoMdSend } from "react-icons/io";
import styled from "styled-components";
import Picker from "emoji-picker-react";

export default function ChatInput({ handleSendMsg, opponentTyping, setAudioInput, setVideoInput }) {
  const [msg, setMsg] = useState("");
  const [showEmojiPicker, setShowEmojiPicker] = useState(false);
  const [isTyping, setIsTyping] = useState(false);
  const typingTimeoutRef = React.useRef(null);

  const handleEmojiPickerhideShow = () => {
    setShowEmojiPicker(!showEmojiPicker);
  };

  const handleEmojiClick = (event, emojiObject) => {
    let message = msg;
    message += emojiObject.emoji;
    setMsg(message);
  };

  const sendChat = (event) => {
    event.preventDefault();
    if (msg.length > 0) {
      handleSendMsg(msg);
      setMsg("");
    }
  };

  useEffect(() => {
    if (isTyping) {
      // Simulated typing indicator for opponent
      opponentTyping(true);

      // Clear the previous timeout
      clearTimeout(typingTimeoutRef.current);

      // Set a timeout to simulate typing
      typingTimeoutRef.current = setTimeout(() => {
        setIsTyping(false);
        opponentTyping(false); // Indicate that the user stopped typing
      }, 2000); // Simulated timeout
    }
  }, [isTyping, opponentTyping]);

  const handleInputChange = (e) => {
    setMsg(e.target.value);
    if (e.target.value.length > 0) {
      setIsTyping(true);
    } else {
      setIsTyping(false);
      opponentTyping(false); // Indicate that the user stopped typing
    }
  };

  return (
    <Container>
      <div className="button-container">
        <div className="emoji">
          <BsEmojiSmileFill onClick={handleEmojiPickerhideShow} />
          {showEmojiPicker && <Picker onEmojiClick={handleEmojiClick} />}
        </div>
      </div>
      <form className="input-container" onSubmit={(event) => sendChat(event)}>
        {isTyping && <p style={{ color: "white" }}>Typing...</p>}
        <input
          type="text"
          placeholder="type your message here"
          onChange={handleInputChange}
          value={msg}
        />
        <button type="submit">
          <IoMdSend />
        </button>
      </form>
    </Container>
  );
}

